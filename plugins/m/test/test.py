import unittest

from . import parse_version

# Tests the parse_version utility, just to be sure it's not giving some
# surprises compared to docutils.version.LooseVersion

class ParseVersion(unittest.TestCase):
    def test(self):
        self.assertEqual(parse_version("3.5.71"), (3, 5, 71))
        self.assertLess(parse_version("4"), (5, ))
        self.assertLess(parse_version("4"), (4, 1))
        self.assertLess(parse_version("4.1"), (5, ))
